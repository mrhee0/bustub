#include <unordered_map>
#include <unordered_set>
#include <vector>

#include "common/config.h"
#include "common/macros.h"

namespace bustub {

enum class AccessType { Unknown = 0, Lookup, Scan, Index };

class LRUKNode {
 private:
  std::list<size_t> history_{};
  frame_id_t fid_;

 public:
  LRUKNode() = default;

  explicit LRUKNode(frame_id_t fid) : fid_(fid) {};

  void set_fid(frame_id_t fid) { fid_ = fid; }

  auto history_size() { return history_.size(); }

  void add_timestamp(size_t timestamp) { history_.push_back(timestamp); }

  void remove_oldest_timestamp() { history_.pop_front(); }

  size_t get_front() { return history_.front(); }
};

/**
 * LRUKReplacer implements the LRU-k replacement policy.
 *
 * The LRU-k algorithm evicts a frame whose backward k-distance is maximum
 * of all frames. Backward k-distance is computed as the difference in time between
 * current timestamp and the timestamp of kth previous access.
 *
 * A frame with less than k historical references is given
 * +inf as its backward k-distance. When multiple frames have +inf backward k-distance,
 * classical LRU algorithm is used to choose victim.
 */
class LRUKReplacer {
 public:
  explicit LRUKReplacer(size_t num_frames, size_t k);

  DISALLOW_COPY_AND_MOVE(LRUKReplacer);

  /**
   * @brief Destroys the LRUReplacer.
   */
  ~LRUKReplacer() {
    node_store_.clear();
    evictables_.clear();
  }

  auto Evict() -> std::optional<frame_id_t>;

  void RecordAccess(frame_id_t frame_id, AccessType access_type = AccessType::Unknown);

  void SetEvictable(frame_id_t frame_id, bool set_evictable);

  void Remove(frame_id_t frame_id);

  auto Size() -> size_t;

 private:
  std::unordered_map<frame_id_t, LRUKNode> node_store_;
  size_t current_timestamp_{0};
  size_t curr_size_{0};
  size_t replacer_size_;
  size_t k_;
  std::mutex latch_;

  //  Evictable frames
  std::unordered_set<frame_id_t> evictables_;
};

}  // namespace bustub
